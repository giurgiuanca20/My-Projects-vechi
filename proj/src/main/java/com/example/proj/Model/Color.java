package com.example.proj.Model;

public class Color {
    private String nrDay;
    private String color;

    public Color() {
    }

    public void setNrDay(String nrDay) {
        this.nrDay = nrDay;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getNrDay() {
        return nrDay;
    }

    public String getColor() {
        return color;
    }
}
